package chap06.textbook.exercises.p16;

public class Printer {
	
	 void println(int n) {
		System.out.println(n);
	}
	 
	 void println(double n) {
		 System.out.println(n);
	 }
	 
	 boolean println(boolean n) {
		 System.out.println(n);
		 return n;
	 }
	 
	 void println(String s) {
		 System.out.println(s);
	 }
	  
	
}
